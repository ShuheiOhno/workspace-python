# シャローコピー
data = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9],
]

data2 = data.copy()
data2[0][0] = 100
print(data)  #[[100, 2, 3], [4, 5, 6], [7, 8, 9]]
print(data2) #[[100, 2, 3], [4, 5, 6], [7, 8, 9]]
print('***********************')

# ディープコピー
import copy
data3 = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9],
]

data4 = copy.deepcopy(data3)
data4[0][0] = 100
print(data3)  #[[1, 2, 3], [4, 5, 6], [7, 8, 9]]
print(data4) #[[100, 2, 3], [4, 5, 6], [7, 8, 9]]